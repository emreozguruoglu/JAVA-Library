private void AraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AraActionPerformed
    String aranılanyer = null;
    String aranan = null;
    
    if (jComboBox1.getSelectedItem().equals("Barkod")) {
            aranılanyer = "barkod";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Ad")){
            aranılanyer = "ad";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Yazarı")){
            aranılanyer = "yazari";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Basım Yılı")){
            aranılanyer = "basimyili";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Turu")){
            aranılanyer = "turu";
            aranan = jTextField7.getText();
        }
        
        try {
            

            
            String sql ="select * from yayinlar where " + aranılanyer + " like'%" + aranan + "%'";
            pst=baglan.prepareStatement(sql);
            rs=pst.executeQuery();
    
            YayinTablosu.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception hata) {
            JOptionPane.showMessageDialog(rootPane, hata.toString());
        }
}//GEN-LAST:event_AraActionPerformed

