private void SilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SilActionPerformed

    int silKontrol = JOptionPane.showConfirmDialog(null, "Silmek istediginizden emin misiniz?","Silindi", JOptionPane.YES_NO_OPTION);
    if(silKontrol==0){
    String sql = "delete from personeller where personelno = ?";
    
    try{
        pst=baglan.prepareStatement(sql);
        
        pst.setString(1, jTextField1.getText());
        
        pst.execute();
        JOptionPane.showMessageDialog(null, "Basariyla silindi");
        
        Goster();
        KutulariTemizle();
    
    }
    
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    }
    
}//GEN-LAST:event_SilActionPerformed

private void PersonelTablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PersonelTablosuMouseClicked

    try{
        int satir=PersonelTablosu.getSelectedRow();
        String Table_click=(PersonelTablosu.getModel().getValueAt(satir, 0).toString());
        String sql="select * from personeller where personelno='"+Table_click+"'" ;
        pst=baglan.prepareStatement(sql);
        rs=pst.executeQuery();
        
    if(rs.next()){
        
        jTextField1.setText(rs.getString("personelno"));
        jTextField2.setText(rs.getString("ad"));
        jTextField3.setText(rs.getString("soyad"));
        jTextField4.setText(rs.getString("telefon"));
        jTextField5.setText(rs.getString("mail"));
    }
 
    
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    
}//GEN-LAST:event_PersonelTablosuMouseClicked

