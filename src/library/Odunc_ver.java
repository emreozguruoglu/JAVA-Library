    private void Goster() {

        try {
            String sql = "select barkod, ad, yazari, basimyili, turu from yayinlar"
                    + " where kimde='" + jLabel1.getText() + "'";
            satirsayisi = 0;
            pst = baglan.prepareStatement(sql);
            rs = pst.executeQuery();
            UzerindekilerTablosu.setModel(DbUtils.resultSetToTableModel(rs));
            satirSayisiniBul();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }

    private void satirSayisiniBul() {
        try {
            String sql = "select barkod, ad, yazari, basimyili, turu from yayinlar"
                    + " where kimde='" + jLabel1.getText() + "'";
            satirsayisi = 0;
            pst = baglan.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {

                satirsayisi++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    
