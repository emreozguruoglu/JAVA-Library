private void OduncVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OduncVerActionPerformed
    barkodkontrol = null;

    if (jLabel5.getText().equals("öğretmen")) {
        uyeozellikkontrol = 7;
    }
    if (jLabel5.getText().equals("öğrenci")) {
        uyeozellikkontrol = 5;
    }
    if (jLabel5.getText().equals("dışardan")) {
        uyeozellikkontrol = 2;
    }


    if (satirsayisi < uyeozellikkontrol) {

        try {

            String sql2 = "select * from yayinlar where barkod='" + jTextField1.getText() + "'";
            pst = baglan.prepareStatement(sql2);
            rs = pst.executeQuery();
            if (rs.next()) {

                barkodkontrol = rs.getString("barkod");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        if (barkodkontrol != null) {
            try {
                String sql = "update yayinlar set kimde ='" + jLabel1.getText() + "'"
                        + " where barkod='" + jTextField1.getText() + "'";
                pst = baglan.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Ödünç verildi");
                Goster();

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bu barkoda sahip bir kitap yok");
        }

    } else {
        JOptionPane.showMessageDialog(null, "Daha fazla yayına sahip olamaz");
    }
}//GEN-LAST:event_OduncVerActionPerformed

