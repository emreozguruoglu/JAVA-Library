private void BulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BulActionPerformed
    if (jComboBox1.getSelectedItem().equals("Barkod")) {
            aranılanyer = "barkod";
            aranan = jTextField1.getText();
        }else if(jComboBox1.getSelectedItem().equals("Adı")){
            aranılanyer = "ad";
            aranan = jTextField1.getText();
        }else if(jComboBox1.getSelectedItem().equals("Yazarı")){
            aranılanyer = "yazari";
            aranan = jTextField1.getText();
        }else if(jComboBox1.getSelectedItem().equals("Basım Yılı")){
            aranılanyer = "basimyili";
            aranan = jTextField1.getText();
        }else if(jComboBox1.getSelectedItem().equals("Türü")){
            aranılanyer = "turu";
            aranan = jTextField1.getText();
        }
        
        try {
            

            
            String sql ="select * from yayinlar where " + aranılanyer + " like'%" + aranan + "%'";
            pst=baglan.prepareStatement(sql);
            rs=pst.executeQuery();
    
            AramaTablosu.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception hata) {
            JOptionPane.showMessageDialog(rootPane, hata.toString());
        }
}//GEN-LAST:event_BulActionPerformed

