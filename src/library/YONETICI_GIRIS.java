    private void cmd_girisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_girisActionPerformed
        String sorgu="SELECT * FROM admingiris WHERE kullaniciadi=? and parola=?" ;
        try{
            pst=baglan.prepareStatement(sorgu);
            pst.setString(1,txt_kullaniciadi.getText());
            pst.setString(2,txt_parola.getText());
            
            sonuc=pst.executeQuery();
            
         if(sonuc.next()){
            JOptionPane.showMessageDialog(null,"Kulanıcı Adı ve Parola onaylandı..!");
            Admin_Giris bilgi=new Admin_Giris();
            bilgi.setVisible(true);
            close();
          }else{
                JOptionPane.showMessageDialog(null,"Kulanıcı Adı veya Parola hatalı...!");
            }
            }catch(Exception e){
              JOptionPane.showMessageDialog(null,e);
        } 
    }//GEN-LAST:event_cmd_girisActionPerformed

