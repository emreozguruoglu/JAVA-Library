private void AraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AraActionPerformed
    String aranılanyer = null;
    String aranan = null;
    
    if (jComboBox1.getSelectedItem().equals("Üye No")) {
            aranılanyer = "uyeno";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Ad")){
            aranılanyer = "ad";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Soyad")){
            aranılanyer = "soyad";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Telefon")){
            aranılanyer = "telefon";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Mail")){
            aranılanyer = "mail";
            aranan = jTextField7.getText();
        }else if(jComboBox1.getSelectedItem().equals("Üye Özelliği")){
            aranılanyer = "uye_ozelligi";
            aranan = jTextField7.getText();
        }
        try {
            

            
            String sql ="select * from uyeler where " + aranılanyer + " like'%" + aranan + "%'";
            pst=baglan.prepareStatement(sql);
            rs=pst.executeQuery();
    
            UyeTablosu.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception hata) {
            JOptionPane.showMessageDialog(rootPane, hata.toString());
        }
}//GEN-LAST:event_AraActionPerformed

